    private void Informe3_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Informe3_btnActionPerformed
        DefaultTableModel modelo = new DefaultTableModel() {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        DescripcionInforme.setText("INFORME DE COMPRAS");
        try {
            var compras = controller.findComprasPorProyecto();
            if (!compras.isEmpty()) {
                modelo.addColumn("ID COMPRA");
                modelo.addColumn("CONSTRUCTORA");
                modelo.addColumn("BANCO VINCULADO");

                for (ComprasPorProyectoVo compra : compras) {
                    modelo.addRow(new Object[]{compra.getID_Compra(), compra.getConstructura(), compra.getBanco_Vinculado()});
                }
                Tabla.setModel(modelo);
                int[] anchos = {80, 200, 175};
                for (int i = 0; i < Tabla.getColumnCount(); i++) {
                    Tabla.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                }
                Tabla.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            } else {
                System.out.println("\nNo hay compras");
                ventanaAlerta.showMessageDialog(null,
                        "No hay compras para mostrar",
                        "Alerta",
                        ventanaAlerta.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            System.err.println("Ha ocurrido un error: " + e.getMessage());
            ventanaAlerta.showMessageDialog(null,
                    e.getMessage(),
                    "Error en el infrome 3",
                    ventanaAlerta.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Informe3_btnActionPerformed


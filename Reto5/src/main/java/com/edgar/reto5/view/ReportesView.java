    private void TablaInforme1() {
        DefaultTableModel modelo = new DefaultTableModel() {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        DescripcionInforme.setText("INFORME DE LIDERES");
        try {
            var lideres = controller.findInfoLider();
            if (!lideres.isEmpty()) {

                modelo.addColumn("ID");
                modelo.addColumn("NOMBRE");
                modelo.addColumn("PRIMER APELLIDO");
                modelo.addColumn("CIUDAD DE RESIDENCIA");

                for (InfoLiderVo lider : lideres) {
                    modelo.addRow(new Object[]{lider.getID_Lider(), lider.getNombre(), lider.getPrimer_Apellido(), lider.getCiudad_Residencia()});
                }
                Tabla.setModel(modelo);
                int[] anchos = {60, 100, 120, 170};
                for (int i = 0; i < Tabla.getColumnCount(); i++) {
                    Tabla.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                }
                Tabla.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            } else {
                System.out.println("\nNo hay lideres");
                ventanaAlerta.showMessageDialog(null,
                        "No hay lideres para mostrar",
                        "Alerta",
                        ventanaAlerta.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            System.err.println("Ha ocurrido un error: " + e.getMessage());
            ventanaAlerta.showMessageDialog(null,
                    e.getMessage(),
                    "Error en el infrome 1",
                    ventanaAlerta.ERROR_MESSAGE);
        }
    }

